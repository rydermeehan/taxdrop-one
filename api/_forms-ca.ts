// California assessment-appeal registry. Unlike TX/GA/FL there is NO usable
// statewide PDF: LA renumbers the form as AAB-100, and Alameda deems an
// application on a prior revision INVALID. So every CA record is county-specific,
// `delivery` is 'official' (county URL primary, our mirror is only a fallback),
// and an uncovered county gets no PDF at all.
//
// Coverage: 53 counties. The original 5 (Los Angeles, San Francisco,
// Sacramento, Alameda, Contra Costa) were verified 2026-07-20 and have a
// mirrored PDF; the other 48 were added 2026-07-21 from each county's own
// clerk-of-the-board site and carry NO mirror (`pdfPath: ''`) — CA delivery
// is 'official', so the county's own URL is the primary download either way.
// Eight of those 48 (Sonoma, Tulare, Monterey, Placer, Marin, Santa Barbara,
// Santa Cruz, Solano) were held back from the initial batch pending an
// independent re-check of their fee and mailing address; that re-check
// completed 2026-07-21 and they now ship. Marin's mailTo and Santa Barbara's
// fee were corrected during that pass — see their `notes`.
//
// Santa Clara is deliberately absent: every county host is behind a Cloudflare
// bot challenge, so its form URL, fee and mailing address could not be
// verified. It falls through to CA_FORM_FALLBACK, which still gives it the
// BOE-certified Sept 15 deadline. Do not add it from secondary sources.
//
// Deadlines split on whether the county Assessor mails value notices — see
// CA_SEPT15 / caDeadline() below, which cover all 58 counties.

export interface CaCounty {
  form: string;
  formTitle: string;
  /** Our mirror. Empty string = not mirrored; the official county URL is primary. */
  pdfPath: string;
  officialUrl: string;
  authority: string;
  efileUrl: string | null;
  website: string;
  mailTo: string;
  deadline: string;
  fee: string | null;   // null = no filing fee
  paperOnly?: boolean;
  notes: string[];
  verifiedOn: string;
  /**
   * Optional bespoke "how to VIEW the assessor's evidence" text. When absent the
   * UI renders its per-state default. In CA the assessor must furnish the
   * comparable-sales data behind your value on request (R&T §408/§1606).
   */
  evidenceHowTo?: string;
}

