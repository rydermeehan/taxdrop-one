// BOE Letter To Assessors 2026/023 certifies every county's filing deadline under
// R&T §1603. Sept 15 where the Assessor mails value notices to all secured-roll
// taxpayers by Aug 1; otherwise Nov 30. RE-PULL EACH JUNE — counties can switch.
// https://www.boe.ca.gov/proptaxes/pdf/lta26023.pdf
const CA_SEPT15 = new Set<string>([
  'alameda','alpine','inyo','kings','mono','placer','sanfrancisco',
  'sanluisobispo','santaclara','sierra','ventura',
]);

/** Certified filing deadline for ANY California county, covered or not. */
export function caDeadline(slug: string): string {
  return CA_SEPT15.has(slug)
    ? 'July 2 – September 15'
    : 'July 2 – November 30';
}

